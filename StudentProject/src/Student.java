
public class Student {
	String name;
	String gender;
	int age;
	String yearLevel;
	String course;
	
	public Student(String name, int age, String gender,String yearLevel, String course) {
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.yearLevel = yearLevel;
		this.course = course;
	}
}
